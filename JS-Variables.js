//data types:
//Primitive(Store directly in variable)
//Number (int or float)
//String (characters)
//Boolean (true or false)
//Undefined (variable declared but not assigned)
//Null (variable declared but not assigned a value)

//Non-Primitive(Stored as references)
//Object (collection of key-value pairs)
//Array (collection of values)
//Function (callable objects)
